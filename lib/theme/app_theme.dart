  // Additional helper methods for specific design elements

  /// Returns the appropriate shadow for cards based on elevation
  static List<BoxShadow> getCardShadow(
      {bool isDark = false, double elevation = 2.0}) {
    final shadowColor = isDark ? shadowDark : shadowLight;
    return [
      BoxShadow(
        color: shadowColor,
        blurRadius: elevation * 2,
        offset: Offset(0, elevation),
      ),
    ];
  }

  /// Returns gradient for primary CTA button (only place gradients are used)
  static LinearGradient getPrimaryCTAGradient({bool isDark = false}) {
    final primary = isDark ? primaryDark : primaryLight;
    return LinearGradient(
      colors: [
        primary,
        primary.withAlpha(204),
      ],
      begin: Alignment.topLeft,
      end: Alignment.bottomRight,
    );
  }

  /// Returns appropriate proximity indicator colors
  static Color getProximityColor(double distance, {bool isDark = false}) {
    if (distance <= 0.5) {
      return isDark ? successDark : successLight; // Very close - green
    } else if (distance <= 2.0) {
      return isDark ? warningDark : warningLight; // Nearby - amber
    } else {
      return isDark ? textSecondaryDark : textSecondaryLight; // Far - gray
    }
  }

  /// Returns hot deal accent color
  static Color getHotDealColor({bool isDark = false}) {
    return isDark ? accentDark : accentLight;
  }
